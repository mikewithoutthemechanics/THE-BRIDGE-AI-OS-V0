/**
 * BRIDGE AI OS -- Page Economics Tracker
 *
 * Tracks economic value generated per page across all 50+ app pages.
 * Every page interaction generates BRDG value, creating a closed-loop
 * economy where user engagement directly feeds the economic engine.
 *
 * Supabase-backed via @supabase/supabase-js.
 */
'use strict';

const { supabase } = require('./supabase');
const crypto = require('crypto');

// -- BRDG value multipliers per action type ---------------------------------
const ACTION_VALUES = {
  view:      1,
  interact:  5,
  convert:   25,
  refer:     15,
  purchase:  50,
};

// -- Vertical page mappings (page path to vertical) -------------------------
const PAGE_VERTICALS = {
  '/ehsa':         'ehsa',
  '/ehsa.html':    'ehsa',
  '/aurora':       'aurora',
  '/aurora.html':  'aurora',
  '/hospital':     'hospital',
  '/hospital.html':'hospital',
  '/aid':          'aid',
  '/aid.html':     'aid',
  '/ban':          'ban',
  '/ban.html':     'ban',
  '/agriculture':  'agriculture',
  '/agriculture.html': 'agriculture',
  '/supaco':       'supaco',
  '/supaco.html':  'supaco',
  '/defi':         'defi',
  '/defi.html':    'defi',
  '/trading':      'trading',
  '/trading.html': 'trading',
  '/intelligence': 'intelligence',
  '/intelligence.html': 'intelligence',
  '/marketplace':  'defi',
  '/marketplace.html': 'defi',
  '/topology':     'intelligence',
  '/topology.html':'intelligence',
  '/registry':     'intelligence',
  '/registry.html':'intelligence',
  '/terminal':     'trading',
  '/terminal.html':'trading',
  '/control':      'aurora',
  '/control.html': 'aurora',
  '/onboarding':   'supaco',
  '/onboarding.html': 'supaco',
};

// -- Helpers ----------------------------------------------------------------
function genId() {
  return 'pe_' + Date.now() + '_' + crypto.randomBytes(4).toString('hex');
}

function now() {
  return new Date().toISOString();
}

// -- Core Functions ---------------------------------------------------------

/**
 * Record economic value generated by a page interaction.
 * @param {string} page     - Page path (e.g. '/ehsa.html')
 * @param {string} userId   - User identifier
 * @param {string} action   - One of: 'view', 'interact', 'convert', 'refer', 'purchase'
 * @param {number} [brdgValue] - Override BRDG value (defaults to action multiplier)
 * @returns {Promise<object>} the recorded entry
 */
async function recordPageValue(page, userId, action, brdgValue) {
  if (!page) throw new Error('Page is required');
  if (!action) throw new Error('Action is required');

  var validActions = ['view', 'interact', 'convert', 'refer', 'purchase'];
  if (validActions.indexOf(action) === -1) {
    throw new Error('Invalid action. Must be one of: ' + validActions.join(', '));
  }

  var value = (typeof brdgValue === 'number' && brdgValue > 0)
    ? brdgValue
    : (ACTION_VALUES[action] || 1);

  var record = {
    id: genId(),
    page: page,
    user_id: userId || 'anon',
    action: action,
    brdg_value: value,
    ts: now(),
  };

  const { error } = await supabase
    .from('page_economics')
    .insert(record);

  if (error) throw new Error('Failed to record page value: ' + error.message);
  return record;
}

/**
 * Get economic metrics for all pages.
 * @returns {Promise<object>} { pages, totals, action_breakdown }
 */
async function getPageMetrics() {
  // Page metrics -- aggregated via RPC or manual query
  const { data: pages } = await supabase.rpc('page_economics_by_page') || { data: [] };

  // Action breakdown
  const { data: actions } = await supabase.rpc('page_economics_by_action') || { data: [] };

  // Totals
  const { count: totalInteractions } = await supabase
    .from('page_economics')
    .select('*', { count: 'exact', head: true });

  const { data: valueRows } = await supabase
    .from('page_economics')
    .select('brdg_value, page, user_id');

  const allRows = valueRows || [];
  const totalValue = allRows.reduce((sum, r) => sum + (r.brdg_value || 0), 0);
  const pagesActive = new Set(allRows.map(r => r.page)).size;
  const uniqueUsers = new Set(allRows.map(r => r.user_id)).size;

  return {
    pages: pages || [],
    totals: {
      total_interactions: totalInteractions || 0,
      total_value: totalValue,
      pages_active: pagesActive,
      unique_users: uniqueUsers,
    },
    action_breakdown: actions || [],
  };
}

/**
 * Get the highest-value pages.
 * @param {number} [limit=10] - Number of pages to return
 * @returns {Promise<Array>} top pages sorted by total BRDG value
 */
async function getTopPages(limit) {
  const { data } = await supabase.rpc('page_economics_by_page') || { data: [] };
  return (data || []).slice(0, limit || 10);
}

/**
 * Get recent page activity.
 * @param {number} [limit=50] - Number of entries
 * @returns {Promise<Array>}
 */
async function getRecentActivity(limit) {
  const { data, error } = await supabase
    .from('page_economics')
    .select('*')
    .order('ts', { ascending: false })
    .limit(limit || 50);

  if (error) throw new Error('Failed to get recent activity: ' + error.message);
  return data || [];
}

/**
 * Get page value breakdown for a specific user.
 * @param {string} userId
 * @returns {Promise<Array>}
 */
async function getUserPageValue(userId) {
  if (!userId) return [];

  const { data, error } = await supabase
    .from('page_economics')
    .select('page, brdg_value')
    .eq('user_id', userId);

  if (error) return [];

  // Aggregate by page in JS
  const pageMap = {};
  for (const row of (data || [])) {
    if (!pageMap[row.page]) {
      pageMap[row.page] = { page: row.page, total_value: 0, interactions: 0 };
    }
    pageMap[row.page].total_value += row.brdg_value || 0;
    pageMap[row.page].interactions += 1;
  }

  return Object.values(pageMap).sort((a, b) => b.total_value - a.total_value);
}

/**
 * Get the vertical for a given page path.
 * @param {string} page
 * @returns {string|null}
 */
function getVerticalForPage(page) {
  if (!page) return null;
  return PAGE_VERTICALS[page] || PAGE_VERTICALS[page.toLowerCase()] || null;
}

// -- Exports ----------------------------------------------------------------
module.exports = {
  recordPageValue: recordPageValue,
  getPageMetrics: getPageMetrics,
  getTopPages: getTopPages,
  getRecentActivity: getRecentActivity,
  getUserPageValue: getUserPageValue,
  getVerticalForPage: getVerticalForPage,
  PAGE_VERTICALS: PAGE_VERTICALS,
  ACTION_VALUES: ACTION_VALUES,
};
