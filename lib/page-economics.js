/**
 * BRIDGE AI OS — Page Economics Tracker
 *
 * Tracks economic value generated per page across all 50+ app pages.
 * Every page interaction generates BRDG value, creating a closed-loop
 * economy where user engagement directly feeds the economic engine.
 *
 * SQLite-backed via better-sqlite3.
 */
'use strict';

const Database = require('better-sqlite3');
const path = require('path');
const crypto = require('crypto');
const fs = require('fs');

// ── DB ─────────────────────────────────────────────────────────────────────
const DB_PATH = process.env.PAGE_ECONOMICS_DB_PATH
  || path.join(__dirname, '..', 'data', 'page-economics.db');

const dbDir = path.dirname(DB_PATH);
if (!fs.existsSync(dbDir)) fs.mkdirSync(dbDir, { recursive: true });

const db = new Database(DB_PATH);
db.pragma('journal_mode = WAL');

// ── Schema ─────────────────────────────────────────────────────────────────
db.exec(`
  CREATE TABLE IF NOT EXISTS page_economics (
    id          TEXT PRIMARY KEY,
    page        TEXT NOT NULL,
    user_id     TEXT,
    action      TEXT NOT NULL,
    brdg_value  REAL NOT NULL DEFAULT 0,
    ts          TEXT NOT NULL
  );

  CREATE INDEX IF NOT EXISTS idx_pe_page    ON page_economics(page);
  CREATE INDEX IF NOT EXISTS idx_pe_user    ON page_economics(user_id);
  CREATE INDEX IF NOT EXISTS idx_pe_action  ON page_economics(action);
  CREATE INDEX IF NOT EXISTS idx_pe_ts      ON page_economics(ts);
`);

// ── BRDG value multipliers per action type ─────────────────────────────────
const ACTION_VALUES = {
  view:      1,
  interact:  5,
  convert:   25,
  refer:     15,
  purchase:  50,
};

// ── Vertical page mappings (page path to vertical) ─────────────────────────
const PAGE_VERTICALS = {
  '/ehsa':         'ehsa',
  '/ehsa.html':    'ehsa',
  '/aurora':       'aurora',
  '/aurora.html':  'aurora',
  '/hospital':     'hospital',
  '/hospital.html':'hospital',
  '/aid':          'aid',
  '/aid.html':     'aid',
  '/ban':          'ban',
  '/ban.html':     'ban',
  '/agriculture':  'agriculture',
  '/agriculture.html': 'agriculture',
  '/supaco':       'supaco',
  '/supaco.html':  'supaco',
  '/defi':         'defi',
  '/defi.html':    'defi',
  '/trading':      'trading',
  '/trading.html': 'trading',
  '/intelligence': 'intelligence',
  '/intelligence.html': 'intelligence',
  '/marketplace':  'defi',
  '/marketplace.html': 'defi',
  '/topology':     'intelligence',
  '/topology.html':'intelligence',
  '/registry':     'intelligence',
  '/registry.html':'intelligence',
  '/terminal':     'trading',
  '/terminal.html':'trading',
  '/control':      'aurora',
  '/control.html': 'aurora',
  '/onboarding':   'supaco',
  '/onboarding.html': 'supaco',
};

// ── Prepared statements ────────────────────────────────────────────────────
const stmts = {
  insert: db.prepare(`
    INSERT INTO page_economics (id, page, user_id, action, brdg_value, ts)
    VALUES (@id, @page, @user_id, @action, @brdg_value, @ts)
  `),
  pageMetrics: db.prepare(`
    SELECT page,
           COUNT(*) AS interactions,
           SUM(brdg_value) AS total_value,
           AVG(brdg_value) AS avg_value,
           COUNT(DISTINCT user_id) AS unique_users
    FROM page_economics
    GROUP BY page
    ORDER BY total_value DESC
  `),
  topPages: db.prepare(`
    SELECT page,
           COUNT(*) AS interactions,
           SUM(brdg_value) AS total_value,
           AVG(brdg_value) AS avg_value,
           COUNT(DISTINCT user_id) AS unique_users
    FROM page_economics
    GROUP BY page
    ORDER BY total_value DESC
    LIMIT ?
  `),
  actionBreakdown: db.prepare(`
    SELECT action,
           COUNT(*) AS count,
           SUM(brdg_value) AS total_value
    FROM page_economics
    GROUP BY action
    ORDER BY total_value DESC
  `),
  totalValue: db.prepare(`
    SELECT COUNT(*) AS total_interactions,
           SUM(brdg_value) AS total_value,
           COUNT(DISTINCT page) AS pages_active,
           COUNT(DISTINCT user_id) AS unique_users
    FROM page_economics
  `),
  recentActivity: db.prepare(`
    SELECT * FROM page_economics
    ORDER BY ts DESC
    LIMIT ?
  `),
  userPageValue: db.prepare(`
    SELECT page, SUM(brdg_value) AS total_value, COUNT(*) AS interactions
    FROM page_economics
    WHERE user_id = ?
    GROUP BY page
    ORDER BY total_value DESC
  `),
};

// ── Helpers ────────────────────────────────────────────────────────────────
function genId() {
  return 'pe_' + Date.now() + '_' + crypto.randomBytes(4).toString('hex');
}

function now() {
  return new Date().toISOString();
}

// ── Core Functions ─────────────────────────────────────────────────────────

/**
 * Record economic value generated by a page interaction.
 * @param {string} page     - Page path (e.g. '/ehsa.html')
 * @param {string} userId   - User identifier
 * @param {string} action   - One of: 'view', 'interact', 'convert', 'refer', 'purchase'
 * @param {number} [brdgValue] - Override BRDG value (defaults to action multiplier)
 * @returns {object} the recorded entry
 */
function recordPageValue(page, userId, action, brdgValue) {
  if (!page) throw new Error('Page is required');
  if (!action) throw new Error('Action is required');

  var validActions = ['view', 'interact', 'convert', 'refer', 'purchase'];
  if (validActions.indexOf(action) === -1) {
    throw new Error('Invalid action. Must be one of: ' + validActions.join(', '));
  }

  var value = (typeof brdgValue === 'number' && brdgValue > 0)
    ? brdgValue
    : (ACTION_VALUES[action] || 1);

  var record = {
    id: genId(),
    page: page,
    user_id: userId || 'anon',
    action: action,
    brdg_value: value,
    ts: now(),
  };

  stmts.insert.run(record);
  return record;
}

/**
 * Get economic metrics for all pages.
 * @returns {object} { pages, totals, action_breakdown }
 */
function getPageMetrics() {
  var pages = stmts.pageMetrics.all();
  var totals = stmts.totalValue.get();
  var actions = stmts.actionBreakdown.all();

  return {
    pages: pages,
    totals: {
      total_interactions: totals.total_interactions || 0,
      total_value: totals.total_value || 0,
      pages_active: totals.pages_active || 0,
      unique_users: totals.unique_users || 0,
    },
    action_breakdown: actions,
  };
}

/**
 * Get the highest-value pages.
 * @param {number} [limit=10] - Number of pages to return
 * @returns {Array} top pages sorted by total BRDG value
 */
function getTopPages(limit) {
  return stmts.topPages.all(limit || 10);
}

/**
 * Get recent page activity.
 * @param {number} [limit=50] - Number of entries
 * @returns {Array}
 */
function getRecentActivity(limit) {
  return stmts.recentActivity.all(limit || 50);
}

/**
 * Get page value breakdown for a specific user.
 * @param {string} userId
 * @returns {Array}
 */
function getUserPageValue(userId) {
  if (!userId) return [];
  return stmts.userPageValue.all(userId);
}

/**
 * Get the vertical for a given page path.
 * @param {string} page
 * @returns {string|null}
 */
function getVerticalForPage(page) {
  if (!page) return null;
  return PAGE_VERTICALS[page] || PAGE_VERTICALS[page.toLowerCase()] || null;
}

// ── Exports ────────────────────────────────────────────────────────────────
module.exports = {
  recordPageValue: recordPageValue,
  getPageMetrics: getPageMetrics,
  getTopPages: getTopPages,
  getRecentActivity: getRecentActivity,
  getUserPageValue: getUserPageValue,
  getVerticalForPage: getVerticalForPage,
  PAGE_VERTICALS: PAGE_VERTICALS,
  ACTION_VALUES: ACTION_VALUES,
  db: db,
};
